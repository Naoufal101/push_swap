#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	char	*str;

	size = ft_strlen(s1) + 1;
	str = (char *)malloc(size * sizeof(char));
	if (!str)
		return (0);
	ft_strlcpy(str, s1, size);
	return (str);
}
